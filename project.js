function searchData() {
    var searchKeyword = document.getElementById("search").value;
    var resultDiv = document.getElementById("result");
    resultDiv.innerHTML = ""; // 検索結果をリセット

    switch (searchKeyword) {
        case "NHK":
            displayNHKData(resultDiv);
            break;
        case "グルメ":
            displayGourmetData(resultDiv);
            break;
        case "天気":
            displayWeatherData(resultDiv);
            break;
        default:
            resultDiv.innerHTML = "<div class='no-result-box'><p class='no-result'>該当するデータが見つかりませんでした。</p></div>";
    }
}

